# Tipos de Instruções: Expressions, Statements, Assignment

"""
    Expressão / Expression
    Instrução que espera um valor de retorno

    Exemplo:
    1 + 1 = 2
    8 > 10 = Falso
    8 < 10 = Verdadeiro

    Declaração / Statement
    Instrução que prepara o interpretador para uma determinada tarefa mais
    não retorna valor

    Exemplo:
    if, else
    def
    for
    while
    pass

    Atribuição / Assignment
    Instrução que pega o retorno de uma expressão e processa o seu valor
    com intuito de armazenar.

    Exemplo:
    soma = 40 + 2
    soma += 3
    soma -= 3

    40 + 2 -> é uma expressão que retorna um valor
    O Python sempre começa fazendo a soma dos valores e depois e atribuido a "soma"
    então sempre começa da direita para a esquerda.

    =  -> Simbolo de atribuição que pega o valor para armazenar ou processar.

    soma -> Variável onde o valor é armazenado.

    -----------------------

    "Protocolo" significa o que o objeto pode fazer

"""
